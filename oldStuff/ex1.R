modelAndPlot <-
function(x0,y,xmat=NULL,useInt=TRUE){
#
#set up 2X2 plotting region
#
par(mfrow=c(2,2))
#
#plot x0 vs y
#
plot(x0,y)
#
#test if xmat is null, if not, regress (do a least squares fit of y on xmat, pull out the coefficients
#from the data structure ls.str, calculate the prediction (note that the 1st
#coefficient defaults to the y intercept, add a colored line to the data plot
#and plot x0 vs the residuals
#
if(!is.null(xmat)){
  ls.str<-lsfit(xmat,y,intercept=useInt)
  coef0<-ls.str$coef
  print(coef0)
  if (useInt) {
  	pred<-coef0[1] + xmat%*%coef0[-1]
  } else {
    pred<-xmat%*%coef0
  }
  lines(x0,pred,col=2)
  plot(x0,ls.str$res)
}
else{
#
#enter this clause only if xmat=NULL, then do as above using X0 instead of xmat
#
  ls.str<-lsfit(x0,y,intercept=useInt)
  abline(ls.str,col=2)
  plot(x0,ls.str$res)
}
#
#make a quantile quantile plot of the residuals vs a normal distribution
#
qqnorm(ls.str$res)
#
#print out the statistics from the regression,
# 
ls.print(ls.str)
}
